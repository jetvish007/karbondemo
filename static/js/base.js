function hideSignup() {
  var x = document.getElementById("loginDiv");
  var y = document.getElementById("signupDiv");
    x.style.display = "block";
    y.style.display = "none";
  }
function hideLogin() {
  var x = document.getElementById("loginDiv");
  var y = document.getElementById("signupDiv");
    x.style.display = "none";
    y.style.display = "block";
  }
